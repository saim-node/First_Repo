# operators
x = 10
y = 3
print(x + y)  # addition
print(x - y)  # subtraction
print(x * y)  # multiplication
print(x / y)  # division
print(x // y) # floor division (It divides x by y and then rounds the result down to the nearest whole number.)
print(x % y)  # modulus
print(x ** y) # exponentiation
print(-x)      # negation
print(+y)      # unary plus (+y just returns the value of y without changing it.)
print(abs(-x)) # absolute value (abs(...) returns the non-negative value of a number.)
print(round(5.67)) # rounding
print(round(5.67, 1)) # rounding to 1 decimal place
print(pow(x, 2)) # power function
print(divmod(x, y)) # quotient and remainder
print(complex(x, y)) # complex number
print(bin(x)) # binary representation


